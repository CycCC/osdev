#pragma once

#include "types.h"

// ------------------------------------------------------------------------------------------------
typedef struct Link
{
    struct Link* prev;
    struct Link* next;
} Link;

// ------------------------------------------------------------------------------------------------
static inline void link_after(Link* a, Link* x)
{
    Link* p = a;
    Link* n = a->next;
    n->prev = x;
    x->next = n;
    x->prev = p;
    p->next = x;
}

// ------------------------------------------------------------------------------------------------
static inline void link_before(Link* a, Link* x)
{
    Link* p = a->prev;
    Link* n = a;
    n->prev = x;
    x->next = n;
    x->prev = p;
    p->next = x;
}

// ------------------------------------------------------------------------------------------------
static inline void link_remove(Link* a)
{
    Link* p = a->prev;
    Link* n = a->next;
    n->prev = p;
    p->next = n;
    a->next = 0;
    a->prev = 0;
}

// ------------------------------------------------------------------------------------------------
#define link_data(link,T,m) \
    (T*)((char*)link - (unsigned long)(&(((T*)0)->m)))
